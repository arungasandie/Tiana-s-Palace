    private void btnOrdersaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdersaveActionPerformed
        // TODO add your handling code here:
        try {
            con=DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurant","root","");
            st=con.createStatement();

            String sql = "INSERT INTO tOrders (tableID,orderDets,Paymethod,Total,Review) VALUES (?,?,?,?,?)";
            pst =con.prepareStatement(sql);
            pst.setString(1,txtTableid.getText());
            pst.setString(2,taOrderd.getText());
            pst.setString(3, txtPayment.getText());
            pst.setString(4,txtTotal.getText());
            pst.setString(5, txtComment.getText());
            pst.execute();
            JOptionPane.showMessageDialog(this, "Order successfull");
        } catch (SQLException er) {
            JOptionPane.showMessageDialog(this, er.getMessage());
        }

    }//GEN-LAST:event_btnOrdersaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int p = JOptionPane.showConfirmDialog(this, "Do you really want to delete this?","Delete",JOptionPane.YES_NO_OPTION);
        if(p==0){
            try{
                String sql = "DELETE FROM tOrders WHERE torderID= '"+txtID.getText()+"'";
                st=con.prepareStatement(sql);
                st.executeUpdate(sql);
              
              con.close();
                 
                JOptionPane.showMessageDialog(this, "Order deleted");
            }
            catch (SQLException er) {
                JOptionPane.showMessageDialog(this, er.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

